    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        String name=NameTxt.getText().trim();
        String address=addressTxt.getText().trim();
        String tel=telTxt.getText().trim();
        String nic=nicTxt.getText().trim().toUpperCase();
        

        if(name.equals("")||address.equals("")||tel.equals(""))
        {JOptionPane.showMessageDialog(this,"Please fill required fields");
        }else if(tel.length()!=10){
            JOptionPane.showMessageDialog(this,"phone number is not valid");
            
        }else if(nic.length()!=10||!nic.endsWith("V")){
            JOptionPane.showMessageDialog(this,"NIC is not valid");
            
        }
        else{
            Connect c =new Connect();
            String query = "INSERT INTO customer(cus_name, cu_add, cus_phn,NIC) VALUES("
            + "'" + name + "'" + ","
            + "'" + address + "'" + ","
            + "'" + tel + "'" + ","
            + "'" + nic + "'"
            + ")";
            c.setQuery(query,"");
            {JOptionPane.showMessageDialog(this,"Customer Added");
                NameTxt.setText("");
                addressTxt.setText("");
                telTxt.setText("");
                nicTxt.setText("");
            }

        }
    }//GEN-LAST:event_okBtnActionPerformed

