    private void supTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_supTblMouseClicked
        if (evt.getClickCount() == 2) {
            int row = supTbl.getSelectedRow();
            int supid = (int) supTbl.getValueAt(row, 0);
            String supname = supTbl.getValueAt(row, 1).toString();
            String address = supTbl.getValueAt(row, 2).toString();
            String companyphn = supTbl.getValueAt(row, 3).toString();
            String smail = supTbl.getValueAt(row, 4).toString();
            String agentname = supTbl.getValueAt(row, 5).toString();
            String agentphn = supTbl.getValueAt(row, 6).toString();

            String amail = supTbl.getValueAt(row, 7).toString();

            
            if(po != null){
            po.setSupplier(supid,supname);
                        this.dispose();

            }
            else if(sr != null){
            sr.setSupplier(supid,supname);
                        this.dispose();

            }
            else if(gobj != null){
            gobj.setSupplier(supid,supname);
                        this.dispose();

            }else{
                EditSupplier es= new EditSupplier();
                es.setVisible(true);
                getParent().add(es);
                es.toFront();
            es.setinfo(supid, supname,address, companyphn,smail, agentname,  agentphn, amail );
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_supTblMouseClicked

