    private void refreahBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreahBtnActionPerformed
        // TODO add your handling code here:
        Connect c = new Connect(); 
        String query = "SELECT supplier_id,supplier_name, sup_add, sup_phn, sup_mail, agnt_name,  agnt_phn, agnt_mail  FROM supplier"  ;
        ResultSet reset = c.getQuery(query);
        dtm.setRowCount(0);
        try {
            while(reset.next()){
                int id = reset.getInt("supplier_id");
                String cname = reset.getString("supplier_name");
                 String cadd = reset.getString("sup_add");
                String cphn = reset.getString("sup_phn");
                String cmail = reset.getString("sup_mail");
                String aname = reset.getString("agnt_name");
                String aphn = reset.getString("agnt_phn");
                String amail = reset.getString("agnt_mail");


                
                dtm.addRow(new Object[]{id, cname, cadd, cphn,cmail, aname,aphn ,amail});
            }
        } catch (SQLException ex) {
            Logger.getLogger(SupplierList.class.getName()).log(Level.SEVERE, null, ex);
        }
        searchTxt.setText("");
    }//GEN-LAST:event_refreahBtnActionPerformed

