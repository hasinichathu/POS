    private void refreshBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshBtnActionPerformed
        // TODO add your handling code here:
        Connect c = new Connect();
        String query = "SELECT stock_id, stock_name FROM stock";
        ResultSet reset = c.getQuery(query);
        dtm.setRowCount(0);
        try {
            while (reset.next()) {
                int stno = reset.getInt("stock_id");
                String stname = reset.getString("stock_name");
                int t=0;
                dtm.addRow(new Object[]{stno, stname,t});
            }
        } catch (SQLException ex) {
            Logger.getLogger(StockView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        int rowcount = stockTbl.getRowCount();
        int row;
        int no;
        for (row=0; row<rowcount; row++){
//            String id = stockTbl.getValueAt(row, 0).toString();
                no=0;
                String query1 = "SELECT item_item_code FROM stock_has_item WHERE stock_stock_id like'%"+stockTbl.getValueAt(row, 0).toString()+"%' ";
                ResultSet reset1 = c.getQuery(query1);
                    try {
                        while (reset1.next()) {
                            int idd = reset1.getInt("item_item_code");
                           no++;
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(StockView.class.getName()).log(Level.SEVERE, null, ex);
                    }
                stockTbl.setValueAt(no, row, 2);
        }
        
    }//GEN-LAST:event_refreshBtnActionPerformed

