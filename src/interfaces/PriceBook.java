    public void getValues(){
    
        Connect c = new Connect(); 
        String query1 = "SELECT  stock_has_item.discount, stock_has_item.retail_price, stock_has_item.buying_price, item.item_name FROM stock_has_item INNER JOIN item ON stock_has_item.Item_Item_code =item.item_code  " ;
        ResultSet reset2 = c.getQuery(query1);
        try {
            while(reset2.next()){
            String name = reset2.getString("item_name");
            int rprice = reset2.getInt("retail_price");
            int bprice = reset2.getInt("buying_price");
            int dis = reset2.getInt("discount");
            dtm.addRow(new Object[]{ name,rprice,bprice,dis });   
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(ItemViwe.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

