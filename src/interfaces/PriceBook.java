    private void searchTxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTxtKeyReleased
        // TODO add your handling code here:
        Connect c = new Connect();

        String query = "SELECT item_catogory_item_catogory_id, item_code, item_barcode, item_name, item_description FROM item"
        + " WHERE item_name like '%"+searchTxt.getText()+"%'" ;
        ResultSet reset = c.getQuery(query);
        dtm.setRowCount(0);
        try {
            while(reset.next()){
                int catno = reset.getInt("item_catogory_item_catogory_id");
                int itmno = reset.getInt("item_code");
                int barcode = reset.getInt("item_barcode");
                String itmname = reset.getString("item_name");
                String des = reset.getString("item_description");

                dtm.addRow(new Object[]{ itmno,itmname, barcode, catno, des });
            }
        } catch (SQLException ex) {
            Logger.getLogger(ItemViwe.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchTxtKeyReleased

